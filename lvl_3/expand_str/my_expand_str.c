#include <unistd.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int	main(int ac, char **av)
{
	int	i;

	if (ac == 2)
	{
		i = 0;
		while (av[1][i])
		{
			if (av[1][i] == ' ' || av[1][i] == '\t')
				i++;
			if (av[1][i] >= 33 && av[1][i] <= 126)
			{
				write(1, &av[1][i], 1);
				if (av[1][i + 1] == ' ')
				{
					write (1, "   ", 3);
				}
				i++;
			}
		}
	}
}
